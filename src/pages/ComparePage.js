import React from "react";
import Tone from "tone";
import axios from "axios";

import SheetMusic from "../components/SheetMusic";
import {PlayJSON} from "../scripts/PlayJSON";
import {Redirect} from "react-router-dom";

// The page where the user sees the comparison between their input and the actual song
class ComparePage extends React.Component {
  /*
  loading: whether the page is still asking for data from the server or not
  redirect_home: whether the page should prepare to redirect to the home page
  redirect_main: whether the page should prepare to redirect to the main page
  actual_song: the note data on the song generated by the site
  corrected_song: the note data on the user input corrected
  */
  state = {
    loading: true,
    redirect_home: false,
    redirect_main: false,
    actual_song: [],
    corrected_song: []
  };

  componentDidMount() {
    //Sends in the data to check if the user input is correct
    if (!this.props.hasPost()) {
      this.setState({
        redirect_home: true
      });
    } else {
      axios
        .post("/submit", this.props.popPost())
        .then(response => {
          this.setState({
            actual_song: response.data.actual,
            corrected_song: response.data.corrected,
            loading: false
          });
          console.log(response);
        })
        .catch(function(error) {
          console.log(error);
        });
    }
  }

  // Returns the amount of notes correct that the user inputted
  getAmtCorrect = () => {
    return this.state.corrected_song.filter(note => note.correct).length;
  };

  // Returns the total number of notes
  getTotal = () => {
    return this.state.actual_song.length;
  };

  // Redirects to main
  handlePlayAgain = () => {
    Tone.Transport.stop();
    this.setState({
      redirect_main: true
    });
  };

  // Redirects home
  handleBackToStart = () => {
    Tone.Transport.stop();
    this.setState({
      redirect_home: true
    });
  };

  render() {
    if (this.state.redirect_home) {
      return <Redirect to="/" />;
    }
    if (this.state.redirect_main) {
      return <Redirect to="/main" />;
    }
    if (this.state.loading) {
      return <p>loading...</p>;
    }
    return (
      <div className="App">
        <p>
          Percent correct: {this.getAmtCorrect()} / {this.getTotal()}
        </p>
        <SheetMusic
          keyId={1}
          width={window.innerWidth - 10 * 2}
          height={window.innerHeight / 2}
          notes={this.state.actual_song}
          editable={false}
        />
        <button onClick={() => PlayJSON(this.state.actual_song)}>play</button>
        <SheetMusic
          keyId={2}
          width={window.innerWidth - 10 * 2}
          height={window.innerHeight / 2}
          notes={this.state.corrected_song}
          editable={false}
          comparison={true}
        />
        <button onClick={() => PlayJSON(this.state.corrected_song)}>
          play
        </button>
        <button onClick={this.handlePlayAgain}>play again?</button>
        <button onClick={this.handleBackToStart}>back to start</button>
      </div>
    );
  }
}

export default ComparePage;
